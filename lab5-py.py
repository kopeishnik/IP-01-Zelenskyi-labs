a = int(input("Enter a value of a:")) #просимо ввести нижню межу вимірювання
b = int(input("Enter a value of b:")) #просимо ввести верхню межу вимірювання
i = a
for i in range(a, b): #проводимо пошук від а до b
  n = 0
  for j in range(1, i-1):
    if i % j == 0:
      n += j #знаходимо суму дільників числа
  if n == i: #перевіряємо число на досконалість
    print(n) #виводмио число, якщо воно досконале
